    // Simple accordion toggle
    document.querySelectorAll('.accordion-title').forEach(function(btn) {
      btn.addEventListener('click', function() {
        var content = btn.nextElementSibling;
        var arrow = btn.querySelector('.accordion-arrow');
        var isOpen = content.style.display === 'block';
        document.querySelectorAll('.accordion-content').forEach(function(c) {
          c.style.display = 'none';
        });
        document.querySelectorAll('.accordion-arrow').forEach(function(a) {
          a.innerHTML = '&#9660;';
        });
        if (!isOpen) {
          content.style.display = 'block';
          arrow.innerHTML = '&#9650;';
        }
      });
    });
  </script>
